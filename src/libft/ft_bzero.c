#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*str2;
	int		size;

	size = (int) n;
	str2 = s;
	i = 0;
	while (i < size)
	{
		str2[i] = '\0';
		i++;
	}
	s = str2;
}
